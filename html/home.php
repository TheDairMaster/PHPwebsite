<p class="h5 mx-5">
	Ce site utilise l'Open Data d'Agglopolys pour vous présenter des statistiques et graphiques. Il traitera notamment des subventions versées aux associations pour l'achat de vélos électriques et des marchés financiers de la communauté.
</p>